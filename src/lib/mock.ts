function createMockMonthly(data: MockTransactionsType[]) {
  const monthName = [
    { name: 'Jan', number: 0 },
    { name: 'Fev', number: 1 }, 
    { name: 'Mar', number: 2 }, 
    { name: 'Abr', number: 3 },
    { name: 'Mai', number: 4 },
    { name: 'Jun', number: 5 },
    { name: 'Jul', number: 6 },
    { name: 'Ago', number: 7 },
    { name: 'Set', number: 8 }, 
    { name: 'Out', number: 9 },
    { name: 'Nov', number: 10 },
    { name: 'Dez', number: 11 }
  ];
  
  const result: MockMonthlyType[] = [];

  const dates = data.map((item) => new Date(item.date)).sort((a, b) => a.getTime() - b.getTime());
  const minDate = dates[0];
  const maxDate = dates[dates.length - 1];
  
  for (let i = minDate.getMonth(); i <= maxDate.getMonth(); i++) {
    const filtered = data.filter((register) => new Date(register.date).getMonth() === i);

    const variableRevenue = filtered
      .filter((register) => register.typeId === 1 && register.accountId === 2)
      .reduce((accumulator, currentValue) => accumulator + currentValue.amount, 0);
    const fixedRevenue = filtered
      .filter((register) => register.typeId === 1 && register.accountId === 3)
      .reduce((accumulator, currentValue) => accumulator + currentValue.amount, 0);
    const cost = filtered
      .filter((register) => register.typeId === 3)
      .reduce((accumulator, currentValue) => accumulator + currentValue.amount, 0);
    const expense = filtered
      .filter((register) => register.typeId === 2 && register.accountId !== 104)
      .reduce((accumulator, currentValue) => accumulator + currentValue.amount, 0);

    result.push({ 
      month: monthName[i].name, 
      date: `2025-${String(i+1).padStart(2, "0")}-01`, 
      fixedRevenue, 
      variableRevenue,
      expense,
      cost,
    });
  }
  
  const averageOrderValueFixed = result.reduce((acc, cur) => acc + cur.fixedRevenue, 0) / 5;
  const averageOrderValueEvent = result.reduce((acc, cur) => acc + cur.variableRevenue, 0) / 4;

  return { month: result, averageOrderValueFixed, averageOrderValueEvent };
}

